    let toastTimeout;
    function showToast(message, isError = false) {
        const toastBar = document.getElementById('toast-bar');
        if (!toastBar) return;
        
        clearTimeout(toastTimeout);
        
        toastBar.textContent = message;
        toastBar.style.backgroundColor = isError ? 'var(--honda-dark)' : 'var(--success-bg, #10b981)';
        
        toastBar.classList.add('show');
        
        toastTimeout = setTimeout(() => {
            toastBar.classList.remove('show');
        }, 3000); 
    }

    function parseDateOnly(dateStr) {
        if (!dateStr) return null;
        const parts = dateStr.split('-');
        return parts.length === 3 ? new Date(parts[0], parseInt(parts[1], 10) - 1, parts[2]) : null;
    }

    function isWithinAllowedHours() {
        const now = new Date();
        const totalMinutes = now.getHours() * 60 + now.getMinutes();
        return totalMinutes >= 600 && totalMinutes < 1320;
    }

    function hasWithdrawnToday() {
        const lastDateStr = document.getElementById('last_withdrawal_date').value;
        if (!lastDateStr) return false;
        const lastDate = parseDateOnly(lastDateStr);
        const today = new Date();
        return lastDate && lastDate.getFullYear() === today.getFullYear() && lastDate.getMonth() === today.getMonth() && lastDate.getDate() === today.getDate();
    }
    
    const withdrawBtn = document.getElementById('withdrawBtn');
    const statusMsg = document.getElementById('statusMsg');

    function updateButtonState() {
        if (!isWithinAllowedHours()) {
            withdrawBtn.disabled = true;
            statusMsg.innerText = 'Withdrawal is only allowed between 10 AM and 10 PM.';
            return;
        }
        if (hasWithdrawnToday()) {
            withdrawBtn.disabled = true;
            statusMsg.innerText = 'You have already withdrawn today. Please try again tomorrow.';
            return;
        }
        withdrawBtn.disabled = false;
        statusMsg.innerText = '';
    }

    function submitWithdraw() {
        const amount = parseFloat(document.getElementById('amount').value);
        if (!amount || amount < 150) {
            showToast('Minimum withdrawal amount is 150 Rs.', true);
            return;
        }
        if (!isWithinAllowedHours() || hasWithdrawnToday()) {
            updateButtonState();
            showToast(statusMsg.innerText, true);
            return;
        }
        document.getElementById('withdrawForm').submit();
    }
    
