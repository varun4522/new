  <script>
    $(document).ready(function () {
      let page = 1; // Track the page/offset
      let loading = false; // Prevent multiple simultaneous loads
      let noMoreData = false; // Stop further loading when empty result set is returned

      function loadData() {
        var inno = $('.my_post_in').val();
        if (loading || noMoreData) return;
        loading = true;
        var action = 'TransactionHistory';
        $.ajax({
          url: '__transaction_result_data.php',
          type: 'GET',
          data: {
            page: page,
            inno: inno,
            action: action
          },
          beforeSend: function () {
            $('#loader').show();
          },
          success: function (data) {
            if ($.trim(data)) {
              $('.WithdrawalResult').append(data);
              page++;
            } else {
              // No data received, stop further loading
              noMoreData = true;
            }
            loading = false;
            $('#loader').hide();
          },
          error: function () {
            loading = false;
            $('#loader').hide();
          }
        });
      }
      // Initial data load
      loadData();
      $('.my_pbody_in').scroll(function () {
        if ($('.my_pbody_in').scrollTop() + $('.my_pbody_in').height() >= $('.WithdrawalResult').height() - 10) {
          loadData();
          // console.log($('.WithdrawalResult').height());
          // alert('Loading more data...');
        }
      });
    });
